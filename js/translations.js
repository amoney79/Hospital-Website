class TranslationManager {
  constructor() {
    this.currentLang = localStorage.getItem('selectedLang') || 'en';
    this.init();
  }

  init() {
    const selector = document.getElementById('language-selector');
    if (selector) {
      selector.value = this.currentLang;
      selector.addEventListener('change', (e) => {
        this.setLanguage(e.target.value);
      });
    }
    this.updateDirection();
    this.translatePage();
  }

  setLanguage(lang) {
    this.currentLang = lang;
    localStorage.setItem('selectedLang', lang);
    this.updateDirection();
    this.translatePage();
  }

  updateDirection() {
    if (this.currentLang === 'ar') {
      document.body.dir = 'rtl';
      document.body.classList.add('rtl');
    } else {
      document.body.dir = 'ltr';
      document.body.classList.remove('rtl');
    }
  }

  getTranslation(key) {
    if (!key) return null;
    return (translations[this.currentLang] && translations[this.currentLang][key]) ||
      (translations['en'] && translations['en'][key]) || null;
  }

  translatePage() {
    // 1. Text Content & Placeholder (Standard data-i18n)
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = this.getTranslation(key);
      if (text) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = text;
        } else {
          // Use innerHTML if text contains tags, otherwise textContent
          if (text.includes('<')) {
            el.innerHTML = text;
          } else {
            el.textContent = text;
          }
        }
      }
    });

    // 2. Specific Attributes (data-i18n-alt, data-i18n-title, etc.)
    const attributes = ['alt', 'title', 'aria-label', 'value'];
    attributes.forEach(attr => {
      const dataAttr = `data-i18n-${attr}`;
      document.querySelectorAll(`[${dataAttr}]`).forEach(el => {
        const key = el.getAttribute(dataAttr);
        const text = this.getTranslation(key);
        if (text) {
          el.setAttribute(attr, text);
        }
      });
    });

    // 3. Select Options
    document.querySelectorAll('option[data-i18n]').forEach(opt => {
      const key = opt.getAttribute('data-i18n');
      const text = this.getTranslation(key);
      if (text) opt.textContent = text;
    });
  }
}

document.addEventListener('DOMContentLoaded', () => {
  window.translationManager = new TranslationManager();
});
